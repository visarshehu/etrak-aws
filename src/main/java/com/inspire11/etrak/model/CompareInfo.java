package com.inspire11.etrak.model;

public class CompareInfo {

	private String firstName;

	private String secondName;
	
	private Long FirstSurvey;

	private Long SecondSurvey;

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getSecondName() {
		return secondName;
	}

	public void setSecondName(String secondName) {
		this.secondName = secondName;
	}

	public Long getFirstSurvey() {
		return FirstSurvey;
	}

	public void setFirstSurvey(Long firstSurvey) {
		FirstSurvey = firstSurvey;
	}

	public Long getSecondSurvey() {
		return SecondSurvey;
	}

	public void setSecondSurvey(Long secondSurvey) {
		SecondSurvey = secondSurvey;
	}
	
	
	
}
