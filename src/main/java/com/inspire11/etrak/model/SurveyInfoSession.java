package com.inspire11.etrak.model;

public class SurveyInfoSession {

	private String firstSurvey;
	
	private String secondSurvey;
	
	private String surveyId;
	
	public String getFirstSurvey() {
		return firstSurvey;
	}
	public void setFirstSurvey(String firstSurvey) {
		this.firstSurvey = firstSurvey;
	}
	public String getSecondSurvey() {
		return secondSurvey;
	}
	public void setSecondSurvey(String secondSurvey) {
		this.secondSurvey = secondSurvey;
	}
	public String getSurveyId() {
		return surveyId;
	}
	public void setSurveyId(String surveyId) {
		this.surveyId = surveyId;
	}
	
	
}
